    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int bar = jTable1.getSelectedRow();
        
        String a = jTable1.getValueAt(bar, 0).toString();jTextField1.setText(a);
        String b = jTable1.getValueAt(bar, 1).toString();jTextField2.setText(b);
        String c = jTable1.getValueAt(bar, 2).toString();jTextField3.setText(c);
        String d = jTable1.getValueAt(bar, 3).toString();
        SimpleDateFormat format= new SimpleDateFormat("yyyy-MM-dd");
        Date da = null;
        try {
            da = format.parse(d);
        } catch (ParseException ex) {
            Logger.getLogger(B2_InputPasienPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        jDateChooser1.setDate(da);
        String e = jTable1.getValueAt(bar, 4).toString();jTextField4.setText(e);
        String f = jTable1.getValueAt(bar, 5).toString();
        if(f.equals("Laki-Laki")){
                jRadioButton1.getModel().setSelected(true);
            }else if(f.equals("Perempuan")){
                jRadioButton2.getModel().setSelected(true);
            }
    }//GEN-LAST:event_jTable1MouseClicked

