    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            //jDateChooser1.setDateFormatString("yyyy-mm-dd");
            String a = jTextField1.getText();
            String b = jTextField2.getText();
            String c = jTextField3.getText();
            String d = ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText();
            String e = jTextField4.getText();
            String f = "";
            
            if(jRadioButton1.isSelected()==true){
                f="Laki-Laki";
            }else if(jRadioButton2.isSelected()==true){
                f="Perempuan";
            }
            Statement stm = con.createStatement();
                if (a.equals("") || (b.equals("") || (c.equals("") || (e.equals("") )))){
                    JOptionPane.showMessageDialog(null,"There are field that empty \n Field can't be empty!","Error",JOptionPane.ERROR_MESSAGE);
                    JOptionPane.showMessageDialog(null,"Please fill the field again");
                    jTextField1.requestFocus();
                }else{
                    String sql = "select * from data_pasien where kode_pasien = '" + a + "'";
                        ResultSet rs = stm.executeQuery(sql);
                        if (rs.next()) {
                            JOptionPane.showMessageDialog(null, "Sorry\nKode has been registered");
                        } else {
                    JOptionPane.showMessageDialog(null,"SUKSES");
                    sql = "insert into data_pasien (kode_pasien,nama_pasien,alamat,tgllahir,notelp,jkelamin) values (?,?,?,?,?,?)";
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setString(1, "P"+a);
                    ps.setString(2, b);
                    ps.setString(3, c);
                    ps.setString(4, d);
                    ps.setString(5, e);
                    ps.setString(6, f);
                    int x = ps.executeUpdate();
                    if (x > 0) {
                        JOptionPane.showMessageDialog(null, "Your data succesfully registered");
                        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
                        model.addRow(new Object[]{("P")+jTextField1.getText(),jTextField2.getText(),
                        jTextField3.getText(),((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText(),jTextField4.getText(),
                        f});
                        }
                    }
                }
                //con.close();
        }catch(Exception a){
            JOptionPane.showMessageDialog(null,"Ada yang salah \n Berikut error message: "+a);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

